package logica.DT;

public class DTVideoUsuario {
    private String usuario;
    private String video;

    public DTVideoUsuario(String usuario, String video) {
        this.usuario = usuario;
        this.video = video;
    }

    public String getUsuario() {
        return usuario;
    }

    public String getVideo() {
        return video;
    }
    
    
}
